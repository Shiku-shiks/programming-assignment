//program to find sum of five numbers
#include<stdio.h>
int main()
{
int f;
int g;
int h;
int i;
int j;
int sum;
printf("please enter the first number\n");
scanf("%d",&f);
printf("enter the second number\n");
scanf("%d",&g);
printf("enter the third number\n");
scanf("%d",&h);
printf("enter the fourth number\n");
scanf("%d",&i);
printf("enter the fifth number\n");
scanf("%d",&j);
sum=f+g+h+i+j;
printf("the sum of five numbers is %d",sum);
return 0;
}